function draw() {//註解，draw()函式會不斷地執行，直到程式結束。就像在紙上不停地畫畫一樣。
  // background(220);
  // 在畫布中間產生一個圓，圓的顏色為#90e0f3，框線顏色為#0077b6，圓的寬為200。就像在紙上畫一個淡藍色的圓，並用深藍色的筆畫出圓的邊界一樣。
  fill('#90e0f3'); //設定填充顏色為#90e0f3，這是一個淡藍色的顏色。
  stroke('#0077b6'); //設定框線顏色為#0077b6，這是一個深藍色的顏色。
  ellipse(windowWidth/2, windowHeight/2, 200, 200); //在畫布中央畫一個半徑為100的#90e0f3顏色的圓。
  
}
