  // createCanvas(400, 400); //建立一個400x400像素的畫布。如到文具店買一張400x400像素的紙張，這就是我們的畫布。
  // background(220); //設定背景顏色為220，這是一個灰色的顏色。就像在紙上塗上灰色的顏料一樣。
  // background(255, 0, 0); //設定背景顏色為紅色，這是一個RGB顏色模式的顏色。就像在紙上塗上紅色的顏料一樣。
  // 產生一張全螢幕的畫布，並設定背景顏色為#b8b3e9。就像在一張全螢幕的紙上塗上淡紫色的顏料一樣。
  createCanvas(windowWidth, windowHeight); //建立一個全螢幕的畫布。
  background('#b8b3e9');//設定背景顏色為#b8b3e9，這是一個淡紫色的顏色。就像在一張全螢幕的紙上塗上淡紫色的顏料一樣。
